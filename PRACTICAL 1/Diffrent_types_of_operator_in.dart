void main() {
  int a = 2;
  int b = 3;

  var c = a + b;
  print("Sum of a and b is $c");

  var d = a - b;
  print("The difference between a and b is $d");

  var e = -d;
  print("The negation of the difference between a and b is $e");

  var f = a * b;
  print("The product of a and b is $f");

  var g = b / a;
  print("The quotient of a and b is $g");

  var h = b ~/ a;
  print("The integer quotient of b divided by a is $h");

  var i = b % a;
  print("The remainder of b divided by a is $i");
}
